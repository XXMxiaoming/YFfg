package com.yfwl.yfgp.model;

import java.util.Date;

public class Revenue {
	double Ef;//期货收益
	double Ed;//债券收益
	double Es;//股票收益
	double Ec;//现金收益
	double sharesmoney;//股票权益
	double futuresmoney;//期货权益
	Date date;
	
	
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public double getEf() {
		return Ef;
	}
	public void setEf(double ef) {
		Ef = ef;
	}
	public double getEd() {
		return Ed;
	}
	public void setEd(double ed) {
		Ed = ed;
	}
	public double getEs() {
		return Es;
	}
	public void setEs(double es) {
		Es = es;
	}
	public double getEc() {
		return Ec;
	}
	public void setEc(double ec) {
		Ec = ec;
	}
	public double getSharesmoney() {
		return sharesmoney;
	}
	public void setSharesmoney(double sharesmoney) {
		this.sharesmoney = sharesmoney;
	}
	public double getFuturesmoney() {
		return futuresmoney;
	}
	public void setFuturesmoney(double futuresmoney) {
		this.futuresmoney = futuresmoney;
	}
	
	
}
