package com.yfwl.yfgp.model;

public class RaiseInfo {
	float profit;
	Integer stop;
	float fee;
	float feeOrigin;
	float bonus;
	Integer num;
	Integer joinNum;
	Integer stimes;
	Integer ltimes;
	Integer pay;
	Integer floor;
	
	public void setProfit(float profit) {
		this.profit = profit;
	}
	public float getProfit() {
		return profit;
	}
	
	public void setStop(Integer stop) {
		this.stop = stop;
	}
	
	public Integer getStop() {
		return stop;
	}
	
	public void setFee(float fee) {
		this.fee = fee;
	}
	public float getFee() {
		return fee;
	}
	
	public void setFeeOrigin(float feeOrigin) {
		this.feeOrigin = feeOrigin;
	}
	public float getFeeOrigin() {
		return feeOrigin;
	}
	
	public void setBonus(float bonus) {
		this.bonus = bonus;
	}
	public float getBonus() {
		return bonus;
	}
	
	public void setNum(Integer num) {
		this.num = num;
	}
	public Integer getNum() {
		return num;
	}
	
	public void setJoinNum(Integer joinNum) {
		this.joinNum = joinNum;
	}
	public Integer getJoinNum() {
		return joinNum;
	}
	
	public void setStimes(Integer stimes) {
		this.stimes = stimes;
	}
	public Integer getStimes() {
		return stimes;
	}
	
	public void setLtimes(Integer ltimes) {
		this.ltimes = ltimes;
	}
	public Integer getLtimes() {
		return ltimes;
	}
	
	public void setPay(Integer pay) {
		this.pay = pay;
	}
	public Integer getPay() {
		return pay;
	}
	
	public void setFloor(Integer floor) {
		this.floor = floor;
	}
	public Integer getFloor() {
		return floor;
	}

}
