package com.yfwl.yfgp.model;

import java.util.Date;

public class FuturesShares {
	private String name;          //股票或期货名字,
	private Double marketvalue;   //市值,
	private Double posiscale;     //持仓比例,
	private String manyempty;     //多空,
	private Double deposit;       //保证金,
	private Date time ;
	private Double price;
	private String number;//编号
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getMarketvalue() {
		return marketvalue;
	}
	public void setMarketvalue(Double marketvalue) {
		this.marketvalue = marketvalue;
	}
	public Double getPosiscale() {
		return posiscale;
	}
	public void setPosiscale(Double posiscale) {
		this.posiscale = posiscale;
	}
	public String getManyempty() {
		return manyempty;
	}
	public void setManyempty(String manyempty) {
		this.manyempty = manyempty;
	}
	public Double getDeposit() {
		return deposit;
	}
	public void setDeposit(Double deposit) {
		this.deposit = deposit;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	
}
