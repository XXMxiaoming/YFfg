package com.yfwl.yfgp.model;

public class DynContentMeta {
	Integer Id;
	Integer userId;
	Integer userIdMeta;
	Integer metaValue;
	Integer status;
	
	public void setId(Integer Id) {
		this.Id = Id;
	}
	
	public Integer getId() {
		return Id;
	}
	
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	
	public Integer getUserId() {
		return userId;
	}
	
	public void setUserIdMeta(Integer userIdMeta) {
		this.userIdMeta = userIdMeta;
	}
	
	public Integer getUserIdMeta() {
		return userIdMeta;
	}
	
	public void setMetaValue(Integer metaValue) {
		this.metaValue = metaValue;
	}
	
	public Integer getMetaValue() {
		return metaValue;
	}
	
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	public Integer getStatus() {
		return status;
	}

}
